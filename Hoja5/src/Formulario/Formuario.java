    private void boton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton1ActionPerformed
        String name1 = textfield1.getText();
        String name2 = textfield2.getText();
        String name3 = textfield3.getText();
        String name4 = textfield4.getText();
        double a = 5;
        String edad = textfield5.getText();
        String equipo = combobox1.getSelectedItem().toString();
        if (name1.isEmpty() || name2.isEmpty() || name3.isEmpty()
                || name4.isEmpty() || edad.isEmpty() || equipo.isBlank()) {
            JOptionPane.showMessageDialog(this, "Debes de llenar todos los campos.");
        } else {
            textarea1.setText("Nombre: \n" + "    " + name1 + " " + name2 + " " + name3 + " " + name4
                    + "\nEdad: \n"
                    + "      " + edad + " a√±os" + "\nEquipo favorito: \n" + "        " + equipo);
        }
        textarea1.setText("" + a);
    }//GEN-LAST:event_boton1ActionPerformed

